var a = parseFloat(prompt("Digite o 1º número"));
var b = parseFloat(prompt("Digite o 2º número"));

var soma = a+b;
var sub = a-b;
var mult = a*b;
var div = a/b;
var resto = a%b;

alert(a + " + " + b + " = " + soma);
alert(a + " - " + b + " = " + sub);
alert(a + " * " + b + " = " + mult);
alert(a + " / " + b + " = " + div);
alert(a + " % " + b + " = " + resto);